    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (campos()) {
            if (validarMarcos()) {
                if (validarReferencias()) {
                    if (validarSegundos()) {
                        if (alg == null) {
                            alg = new Algoritmos(marcos, lista, segundos,this);
                        }
                        alg.setVisible(true);
                        this.setVisible(false);
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Por favor ingrese los valores en los campos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    public boolean campos() {
        return !jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty();
    }

    public boolean validarMarcos() {
        int marcosInternos = 0;
        try {
            marcosInternos = Integer.parseInt(jTextField1.getText());
            if (marcosInternos < 2 || marcosInternos > 6) {
                JOptionPane.showMessageDialog(this, "Debe ingresar una cantidad de marcos valida");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Debe ingresar un valor numerico en los marcos");
            return false;
        }
        marcos = marcosInternos;
        return true;
    }

    public boolean validarReferencias() {
        String[] partes = jTextField2.getText().split(" ");
        if (partes.length < 2) {
            JOptionPane.showMessageDialog(this, "El tamaÃ±o de lista de referencias es muy corta, tiene " + partes.length + " elementos");
            return false;
        }
        int[] numeros = new int[partes.length];

        for (int i = 0; i < partes.length; i++) {
            try {
                numeros[i] = Integer.parseInt(partes[i].trim());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Error al convertir a entero: " + partes[i]);
                return false;
            }
        }
        lista = numeros;
        return true;
    }

    private boolean validarSegundos() {
        String segundos = tiempoText.getText();
        try {
            int segundosParseados = Integer.parseInt(segundos);
            this.segundos = segundosParseados;
            if (this.segundos >= 10) {
                JOptionPane.showMessageDialog(this, "Ingreso un tiempo muy alto, ingrese un valor menor a 10");
                return false;
            }
            return true;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Porfavor ingrese un valor valido en el tiempo");
            return false;
        }
    }

